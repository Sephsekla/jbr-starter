		<h3>INVOCELL® Intravaginal Culture System</h3>
		<h4>INDICATION FOR USE</h4>
		<p>
			INVOCELL® Intravaginal Culture System consists of the INVOCELL Culture Device and the INVOCELL Retention
			Device.</p>
		<p>The Culture Device is indicated for use in preparing, holding, and transferring human gametes or embryos
			during In Vitro Fertilization/Intravaginal Culture (IVF/IVC) and Intra-Cytoplasmic Sperm Injection
			Fertilization/ Intravaginal Culture (ICSI/IVC) procedures.</p>
		<p>The Retention Device is indicated for use during the incubation period to aid in retention of the Culture
			evice in the vaginal cavity.</p>
		<p>The Culture and Retention Devices are not indicated for incubation periods exceeding 72 hours.
		</p>
		<h4>IMPORTANT SAFETY INFORMATION</h4>
		<ul>
			<li>Culture and Retention Devices should not be used in patients with the inability to tolerate placement or wearing of a device within the vaginal cavity.</li>
	<li>Culture and Retention Devices are single use only. Do not use if product or package appears damaged.</li>
	<li>Do not use Culture Device in patients with hypersensitivity to medical grade silicone or polystyrene or in patients with a severe case of vaginitis or with a history of toxic shock syndrome. Evaluate patients for any recent pelvic surgery to assure it will not affect intravaginal culture procedure.</li>
	<li>Proper handling is extremely important to safe and effective use of Culture Device. Do not begin clinical use of INVOCELL Intravaginal Culture System without establishing competency by reading and practicing Instructions for Use.</li>
	<li>It is recommended that INVOCELL Intravaginal Culture System be utilized with a mild ovarian stimulation protocol. The recommended upper limit on number of oocytes or ICSI fertilized embryos to be placed in Culture Device is seven.</li>
	<li>Verify Culture Device is correctly locked before placement in vaginal cavity.</li>
	<li>Patients should avoid any activity that may alter temperature of vaginal cavity and should avoid manipulation and removal of Culture and Retention Devices while in place.</li>
		</ul>
