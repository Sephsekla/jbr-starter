		<h3>ENDOMETRIN® <br />
			(progesterone) Vaginal Insert </h3>
		<h4>INDICATION FOR USE</h4>
		<ul>
			<li>ENDOMETRIN® (progesterone) Vaginal Insert is indicated to support embryo implantation and early
				pregnancy by supplementation of corpus luteal function as part of an Assisted Reproductive Technology
				(ART) treatment program for infertile women.</li>
		</ul>
		<h4>IMPORTANT SAFETY INFORMATION</h4>
		<ul>
			<li>ENDOMETRIN should not be used in individuals with any of the following conditions: previous allergic
				reactions to progesterone or any of the ingredients of ENDOMETRIN, undiagnosed vaginal bleeding, known
				missed abortion or ectopic pregnancy, liver disease, known or suspected breast cancer, active arterial
				or venous thromboembolism or severe thrombophlebitis, or a history of these events.
			</li>
			<li>The physician should be alert to earliest signs of myocardial infarction, cerebrovascular disorders,
				arterial or venous thromboembolism (venous thromboembolism or pulmonary embolism), thrombophlebitis, or
				retinal thrombosis. ENDOMETRIN should be discontinued if any of these are suspected.
			</li>
			<li>Patients with a history of depression need to be closely observed. Consider discontinuation if symptoms
				worsen.
			</li>
			<li>ENDOMETRIN should not be recommended for use with other vaginal products (such as antifungal products)
				as this may alter progesterone release and absorption from the vaginal insert.
			</li>
			<li>The most common adverse reactions reported (greater than 5%) were post-oocyte retrieval pain, abdominal
				pain, nausea, and ovarian hyperstimulation syndrome.
			</li>
		</ul>
