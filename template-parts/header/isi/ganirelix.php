		<h3>Ganirelix Acetate Injection</h3>
			<h4>INDICATION FOR USE</h4>
			<ul>
				<li>Ganirelix acetate injection is indicated for the inhibition of premature LH surges in women
					undergoing controlled ovarian hyperstimulation.</li>
			</ul>
			<h4>IMPORTANT SAFETY INFORMATION</h4>
			<ul>
				<li>Ganirelix acetate injection is contraindicated under the following conditions: Known
					hypersensitivity to ganirelix acetate or to any of its components, Known hypersensitivity to GnRH or
					any other GnRH analog, Known or suspected pregnancy.</li>
				<li>Ganirelix acetate injection should be prescribed by physicians who are experienced in infertility
					treatment. Before starting treatment with ganirelix acetate, pregnancy must be excluded. Safe use of
					ganirelix acetate during pregnancy has not been established.</li>
				<li>Special care should be taken in women with signs and symptoms of active allergic conditions.</li>
				<li>Cases of hypersensitivity reactions, including anaphylactoid reactions, have been reported, as early
					as with the first dose, during postmarketing surveillance. In the absence of clinical experience,
					ganirelix acetate treatment is not advised in women with severe allergic conditions.</li>
				<li>The packaging of this product contains natural rubber latex which may cause allergic reactions.</li>
				<li>Most commonly reported adverse reactions (≥ 1%) reported in clinical trials were: Abdominal Pain
					(gynecological), Death Fetal, Headache, Ovarian Hyperstimulation Syndrome, Vaginal Bleeding,
					Injection Site Reaction, Nausea, Abdominal Pain (gastrointestinal).</li>
				<li>During post marketing surveillance, rare cases of hypersensitivity reactions, including
					anaphylactoid reactions, have been reported, as early as with the first dose.</li>
				<li>Major and minor congenital abnormalities have been reported in clinical follow-up studies of
					newborns of women administered ganirelix acetate injection. The causal relationship between these
					congenital anomalies and ganirelix acetate is unknown.</li>
			</ul>
