		<h3>NOVAREL® <br />(chorionic gonadotropin for injection, USP) </h3>
		<h4>INDICATION FOR USE</h4>
		<ul>
			<li>NOVAREL® is indicated for the induction of ovulation and pregnancy in the anovulatory, infertile woman
				in whom the cause of anovulation is secondary and not due to primary ovarian failure, and who has been
				appropriately pre-treated with human menotropins.</li>
		</ul>
		<h4>IMPORTANT SAFETY INFORMATION</h4>
		<ul>
			<li>NOVAREL should not be used in patients with prior allergic reaction to HCG. HCG may cause fetal harm
				when administered to a pregnant woman.</li>
			<li>NOVAREL should be used in conjunction with human menopausal gonadotropins only by physicians experienced
				with infertility problems who are familiar with the criteria for patient selection, contraindications,
				warnings, precautions, and adverse reactions described in the package insert for menotropins. The
				principal serious adverse reactions during this use are: (1) Ovarian hyperstimulation, a syndrome of
				sudden ovarian enlargement, ascites with or without pain, and/or pleural effusion; (2) Enlargement of
				preexisting ovarian cysts or rupture of ovarian cysts with resultant hemoperitoneum; (3) Multiple
				births, and (4) Arterial thromboembolism.</li>
			<li>Anaphylaxis has been reported with urinary-derived HCG products.</li>
			<li>Since androgens may cause fluid retention, HCG should be used with caution in patients with cardiac or
				renal disease, epilepsy, migraine, or asthma.</li>
			<li>HCG can cross react in the radioimmunoassay of gonadotropins, especially luteinizing hormone. Each
				individual laboratory should establish the degree of cross reactivity with their gonadotropin assay.
				Physicians should make the laboratory aware of patients on HCG if gonadotropin levels are requested.
			</li>
			<li>Defects of forelimbs and of the central nervous system, as well as alterations in sex ratio, have been
				reported in mice on combined gonadotropin and HCG regimens. The dose of gonadotropin used was intended
				to induce superovulation. No mutagenic effect has been clearly established in humans.</li>
			<li>It is not known whether this drug is excreted in human milk. Because many drugs are excreted in human
				milk caution should be exercised when HCG is administered to a nursing woman.</li>
			<li>Adverse reactions include headache, irritability, restlessness, depression, fatigue, edema, precocious
				puberty, gynecomastia, pain at the site of injection. Hypersensitivity reactions both localized and
				systemic in nature, including erythema, urticaria, rash, angioedema, dyspnea and shortness of breath,
				have been reported. The relationship of these allergic-like events to the polypeptide hormone or the
				diluent containing benzyl alcohol is not clear.</li>
		</ul>
